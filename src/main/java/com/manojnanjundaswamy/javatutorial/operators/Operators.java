public class Operators {

//	 Below, we use the + operator to add together two values
	int x = 100 + 50;

	int sum1 = 100 + 50; // 150 (100 + 50)
	int sum2 = sum1 + 250; // 400 (150 + 250)
	int sum3 = sum2 + sum2; // 800 (400 + 400)

}
