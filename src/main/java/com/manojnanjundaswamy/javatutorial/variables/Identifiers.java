package com.manojnanjundaswamy.javatutorial.variables;

/**
 * All Java variables must be identified with unique names.<br>
 * 
 * These unique names are called identifiers.<br>
 * 
 * Identifiers can be short names (like x and y) or more descriptive names (age,
 * sum, totalVolume).<br>
 * 
 * <strong>Note: It is recommended to use descriptive names in order to create
 * understandable and maintainable code:</strong> *
 * 
 * @author Manoj Nanjundaswamy
 *
 */
public class Identifiers {

	// Good
	int minutesPerHour = 60;

	// OK, but not so easy to understand what m actually is
	int m = 60;
}
